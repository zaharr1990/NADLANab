// Live call to Gemini API using browser fetch
async function queryGeminiAPI(apiKey, agentId, chatHistory, userMessage) {
    const agent = AGENTS_DATA[agentId];
    if (!agent) throw new Error("סוכן לא נמצא");
    
    const url = `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key=${apiKey}`;
    
    // Format history for Gemini structure
    // chatHistory is [{role: 'user'|'model', text: '...'}]
    const contents = [];
    
    // Add history
    chatHistory.forEach(msg => {
        contents.push({
            role: msg.role === 'user' ? 'user' : 'model',
            parts: [{ text: msg.text }]
        });
    });
    
    // Add current user message
    contents.push({
        role: 'user',
        parts: [{ text: userMessage }]
    });
    
    const requestBody = {
        contents: contents,
        systemInstruction: {
            parts: [{ text: agent.systemPrompt }]
        },
        generationConfig: {
            temperature: 0.7,
            maxOutputTokens: 1500
        }
    };
    
    try {
        const response = await fetch(url, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json'
            },
            body: JSON.stringify(requestBody)
        });
        
        if (!response.ok) {
            const errData = await response.json();
            console.error("Gemini API Error details:", errData);
            throw new Error(errData.error?.message || `שגיאת תקשורת (${response.status})`);
        }
        
        const data = await response.json();
        const responseText = data.candidates?.[0]?.content?.parts?.[0]?.text;
        
        if (!responseText) {
            throw new Error("לא התקבלה תשובה משרת ה-AI");
        }
        
        return responseText;
    } catch (error) {
        console.error("Error calling Gemini API:", error);
        throw error;
    }
}
